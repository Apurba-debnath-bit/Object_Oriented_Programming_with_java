
package Var_args_Method;


public class Addition {
    void add(int a, int b){
        System.out.println(a+b);
    }
    void add(int a, int b, int c){
        System.out.println(a+b);
    }
    void add(int a, int b, int c, int d){
        System.out.println(a+b);
    }
    void add(int a, int b, int c, int d, int e){
        System.out.println(a+b);
    }
}
